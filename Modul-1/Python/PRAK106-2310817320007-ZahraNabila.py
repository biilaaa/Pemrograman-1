a = int(input("Variabel a bernilai = "))

b = int(input("Variabel b bernilai = "))

c = int(input("Variabel c bernilai = "))

aSamaDenganb = a == b
bLebihBesarDaric = b > c
aTidakSamaDenganc = a != c

print(f"Apakah a sama dengan b ? jawabannya adalah  {aSamaDenganb}")
print(f"Apakah b lebih besar dari c ? jawabannya adalah  {bLebihBesarDaric}")
print(f"Apakah a tidak sama dengan c ? jawabannya adalah  {aTidakSamaDenganc}")